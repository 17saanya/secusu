'use strict';

var User = function User(args) {
    if (typeof args.name == 'undefined') {
        var NameGenerator = require('./random-names');
        args.name = new NameGenerator().make(args.excludeNames);
    }

    this.name = args.name;

    this.rooms = [];

    this.socketId = args.socketId;
};

module.exports = User;
